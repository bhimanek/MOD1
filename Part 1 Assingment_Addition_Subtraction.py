# Program to perform addition and subtraction of two numbers

# Asking the user to input two numbers
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# Calculating the addition
addition = num1 + num2

# Calculating the subtraction
subtraction = num1 - num2

# Displaying the results
print("The addition of", num1, "and", num2, "is:", addition)
print("The subtraction of", num1, "and", num2, "is:", subtraction)
